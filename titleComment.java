import greenfoot.*;  // (World, Actor, GreenfootImage, Greenfoot and MouseInfo)

/**
 * Creates title comment picture.
 * 
 * @author Ben Johnson 
 * @version 11/4/16
 */
public class titleComment extends Actor
{
    /**
     * Construtor creates picture for the title
     * 
     * @param none
     * @return nothing
     */
    public titleComment()
    {
        GreenfootImage title = new GreenfootImage("comment.png");
        setImage(title);
    }
}
